		<b>Historia</b><br>
		Tras la conquista, la corona española se enfocó en la catequización de los indígenas. Las congregaciones<br> 
		fundadas por los misioneros reales en el Nuevo Mundo fueron llamadas «doctrinas de indios» o simplemente<br> «doctrinas». Originalmente, los frailes tenían únicamente una misión temporal: enseñarle la fe católica a <br>
		los indígenas, para luego dar paso a parroquias seculares como las establecidas en España; con este fin, los<br> frailes debían haber enseñado los evangelios y el idioma español a los nativos. Ya cuando los indígenas <br>
		estuvieran catequizados y hablaran español, podrían empezar a vivir en parroquias y a contribuir con el <br>
		diezmo, como hacían los peninsulares.<br><br>

		Pero este plan nunca se llevó a cabo, principalmente porque la corona perdió el control de las órdenes<br> 
		regulares tan pronto como los miembros de éstas se embarcaron para América. Protegidos por sus privilegios<br> apostólicos para ayudar a la conversión de los indígenas, los misionares solamente atendieron a la autoridad <br>
		de sus priores y provinciales, y no a la de las autoridades españolas ni a las de los obispos. Los provinciales <br>
		de las órdenes, a su vez, únicamente rendían cuentas a los líderes de su orden y no a la corona. Una vez habían<br> establecido una doctrina, protegían sus intereses en ella, incluso en contra de los intereses del rey y de esta<br> forma las doctrinas pasaron a ser pueblos de indios que se quedaron establecidos para todo el resto de la<br> 
		colonia.<br><br>

		Las doctrinas fueron fundadas a discreción de los frailes, ya que tenían libertad completa para establecer<br> comunidades para catequizar a los indígenas, con la esperanza de que estas comunidades pasaran con el tiempo<br> 
		a la jurisdicción de una parroquia secular a la que se le pagaría el diezmo. En realidad, lo que ocurrió fue<br> 
		que las doctrinas crecieron sin control y nunca pasaron al control de parroquias; se formaron alrededor de una<br> cabecera en donde tenían su monasterio permanente los frailes y de dicha cabecera salían los frailes a <br>
		catequizar o visitar las aldeas y caseríos que pertenecían a la doctrina, y que se conocían como anexos,<br> 
		visitas o pueblos de visita. Así pues, las doctrinas tenían tres características principales:<br><br>

		-eran independientes de controles externos (tanto civiles como eclesiásticos)<br>
		-eran administradas por un grupo de frailes<br>
		-tenían un número relativamente grande de anexos<br><br>
