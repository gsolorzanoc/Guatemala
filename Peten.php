	<br><br>
    <font face="A little sunshine" size="5" color="white">
    	Este departamento posee suelos formados en alto porcentaje por aluviones cuaternarios, eocenos, paleoceno-eoceno, cretacico, y la presencia de varias fallas, que provocan los movimientos teluricos. El departamento comprende varias cuencas marinas sedimentarias, dentro de las cuales se depositaron desde fines de la Era Paleozoica, hace mas de 200 millones de años, grandes espesores de rocas sedimentarias que hoy dia componen el subsuelo de la region.

Dentro de las rocas que afloran en la superficie se encuentran especificamente profusas rocas calizas que corresponden al periodo cretacico y que se originaron hace aproximadamente 100 millones de años.

La topografia del departamento es levemente variada, y se divide en tres zonas:

Región Baja: Corresponde a las llanuras y los lagos, donde se encuentra la mayoria de la poblacion del departamento, asi como sus grandes praderas o sabanas.
Region Media: Corresponde las primeras alturas, incluyendo las pequeñas colinas de los valles de los rios San Pedro, Azul y San Juan.
Region Alta: Corresponde las Montes Maya en la parte oriental del departamento, la de mayor elevacion. Por sus condiciones climaticas siempre ha sido una zona favorable para el establecimiento de la ganaderia y colonias agricolas. </font>
