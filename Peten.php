    	Peten (en itzá: Noj Peten, ‘Gran Isla’)? es uno de los veintidos departamentos que conforman Guatemala, situado en el extremo septentrional del pais. Limita al norte con Mexico con los estados de Quintana Roo, Campeche; al sur con los departamentos de Izabal y Alta Verapaz; al sureste con el Departamento del Quiche; al este con Belice con los distritos de Cayo y Toledo; y al oeste con Mexico con los estados de Tabasco y Chiapas. Posee una extension territorial de 35.854 km², lo que lo convierte en el departamento mas extenso de Guatemala asi como en la entidad sub-nacional más grande de Centroamerica.

