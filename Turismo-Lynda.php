		<b>Origen</b><br>
		El lago de Amatitlán tiene su origen en la Era Cuaternaria y su formación se debe a los movimientos <br>
		tecto-volcánicos ocurridos en el área y provocados por los volcanes de Pacaya, Agua, Fuego y Acatenango.<br> 
		Durante esa época, su extensión era de aproximadamente 80km2 , ocupando la superficie de los municipios de<br>
		Amatitlán, Villa Nueva y Villa Canales ya que se han encontrado varios fósiles de caracoles y pequeños<br>
		esqueletos de peces durante excavaciones realizadas en esos lugares a principios de siglo. Durante la época<br>
	 	precolombina, el lago fue la fuente principal de abastecimiento de agua y de alimento para las tribus Pocomam<br> asentadas a su alrededor. <br><br>

		<b>Contaminación del Lago</b><br>
		El río Villalobos que alimenta el lago, es uno de los principales desagües de aguas servidas domésticas, <br>industriales y agroindustriales del área metropolitana de la Ciudad de Guatemala. Cada año un volumen <br>
		importante de aguas negras y 500,000 metros cúbicos de sedimentos entran en el lago.5​6​ Esta situación ha <br>
		resultado en altos niveles de contaminación y una acelerada eutrofización y sedimentación, afectando las<br> 
		funciones del lago como fuente de agua potable, para la pesca y el riego, y ha reducido sus funciones<br> 
		recreativas.<br><br>
